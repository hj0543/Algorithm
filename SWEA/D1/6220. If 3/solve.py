import sys
sys.stdin = open('input.txt', 'r')

'''
복습필요
'''

# 1. 첫 번째 입력값(N)을 정수형으로 변환하여 저장합니다.
# 이 값은 앞으로 몇 번의 테스트(반복)를 수행할지 결정하는 기준이 됩니다.
N = int(input())

# 2. 1부터 N까지 반복하는 루프를 시작합니다. (i는 현재 몇 번째 테스트인지를 나타냄)
# range(1, N + 1)을 사용하는 이유는 문제 출력 형식인 #1, #2...를 맞추기 위함입니다.
for i in range(1, N + 1):

    # 3. 한 줄에 들어온 문자(알파벳) 하나를 입력받습니다.
    # .strip()은 혹시 모를 앞뒤 공백이나 줄바꿈 문자를 제거하여 순수 문자만 남깁니다.
    words = input().strip()

    # 4. .isupper() 메서드를 사용하여 입력받은 문자가 대문자인지 확인합니다.
    if words.isupper():
        # 문자가 대문자('A'-'Z')라면 변수에 '대문자' 저장
        loup = '대문자'
    else:
        # 그 외의 경우(소문자 'a'-'z')라면 변수에 '소문자' 저장
        loup = '소문자'

    # 5. f-string(포맷팅)을 사용하여 요구하는 출력 형식에 맞춰 결과를 출력합니다.
    # i: 케이스 번호, words: 입력받은 문자, loup: 판별 결과
    print(f'#{i} {words} 는 {loup} 입니다.')